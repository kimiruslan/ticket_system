<?php
session_start();
include('config.php');

// If already logged in, redirect to dashboard
if (isset($_SESSION['technician_id'])) {
    header('Location: dashboard.php');
    exit();
}

$error = '';

// Handle login form submission
if ($_SERVER['REQUEST_METHOD'] == 'POST' && isset($_POST['login'])) {
    $email = trim($_POST['email']);
    $password = $_POST['password'];
    
    if (!empty($email) && !empty($password)) {
        // Check if technician exists
        $stmt = $conn->prepare("SELECT id, name, email, password FROM technicians WHERE email = ?");
        $stmt->bind_param("s", $email);
        $stmt->execute();
        $result = $stmt->get_result();
        
        if ($result->num_rows == 1) {
            $technician = $result->fetch_assoc();
            
            // Verify password
            if (password_verify($password, $technician['password'])) {
                // Set session variables
                $_SESSION['technician_id'] = $technician['id'];
                $_SESSION['technician_name'] = $technician['name'];
                $_SESSION['technician_email'] = $technician['email'];
                
                // Redirect to dashboard
                header('Location: dashboard.php');
                exit();
            } else {
                $error = 'Invalid email or password.';
            }
        } else {
            $error = 'Invalid email or password.';
        }
        $stmt->close();
    } else {
        $error = 'Please fill in all fields.';
    }
}
